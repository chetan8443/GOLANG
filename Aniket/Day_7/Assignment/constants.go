package main

import "fmt"

func main() {
	// Constant Integer type variable is declared and Initialised
	const i1 int = 45

	// Constant Float64 type variable is declared and Initialised
	const f1 float64 = 65.7

	// Constant Boolian type variable is declared and Initialised
	const b1 bool = true

	// Constant String type variable is declared and Initialised
	const s1 string = "Aniket"

	fmt.Println("Integer type variable i1 is declared and initialise as", i1)
	fmt.Println("Float64 type variable f1 is declared and initialise as", f1)
	fmt.Println("Boolian type variable b1 is declared and initialise as", b1)
	fmt.Println("String type variable s1 is declared and initialise as", s1)
}
